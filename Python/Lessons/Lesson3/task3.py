# Время суток
# ===========

h = int(input('Который час? - '))
if 4 <= h < 12:
    print('Доброе Утро!')
elif 12 <= h < 17:
    print('Добрый день!')
elif 17 <= h < 24:
    print('Добрый вечер!')
elif 0 <= h < 4 or h == 24:
    print('Спокойной ночи!')
else:
    print('Такого времени не существует!')
