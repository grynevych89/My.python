days = int(input('Введите номер дня недели: '))

if days == 1:
    print('Понедельник')
elif days == 2:
    print('Вторник')
elif days == 3:
    print('Среда')
elif days == 4:
    print('Четверг')
elif days == 5:
    print('Пятница')
elif days == 6:
    print('Суббота')
elif days == 7:
    print('Воскресенье')
else:
    print('Такого дня не существует!')
